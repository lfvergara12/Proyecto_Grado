// Abre el cuadro modal
function abrirResultadoModal() {
    var modal = document.getElementById("resultado");
    modal.style.display = "block";
}

// Cierra el cuadro modal
function cerrarResultadoModal() {
    var modal = document.getElementById("resultado");
    modal.style.display = "none";
}
